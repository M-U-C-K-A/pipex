#include "libft.h"

t_list	*ft_lstnew(void const *content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (!new)
		return (NULL);
	if (!content)
	{
		new->content = NULL;
		new->next = NULL;
		return (new);
	}
	new->content = (void *)content;
	new->next = NULL;
	return (new);
}
